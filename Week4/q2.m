number = input("Enter a number: ");
switch number
    case 1
        disp("|");
    case 2
        disp("||");
    case 3
        disp("|||");
    case 4
        disp("|V");
    case 5
        disp("V");
    case 6
        disp("V|");
    case 7
        disp("V||");
    case 8
        disp("V|||");
    case 9
        disp("|X");
    otherwise
        disp("Enter a number between 1 and 10");     
end